class Mylog() :
   """ 
      Cette classe implemente des fonctions de trace utilisees pour deboger.
   """
   _debug = False
   #---------------------------------------------------------------------------
   #
   #---------------------------------------------------------------------------
   def __init__(self, debug=False) :
      self._debug = debug

   #---------------------------------------------------------------------------

   #---------------------------------------------------------------------------
   #
   #---------------------------------------------------------------------------
   def mylog(self,message) :
      if True == self._debug :
         print(message)
      else :
         pass
   #---------------------------------------------------------------------------
   
   #---------------------------------------------------------------------------
   #
   #---------------------------------------------------------------------------
   def mylog_erreur(self,message) :
      if True ==  self._debug :
         print("\n*** ERREUR ***")
         print(message)
         print("**************\n")
      else :
         pass
   #---------------------------------------------------------------------------


